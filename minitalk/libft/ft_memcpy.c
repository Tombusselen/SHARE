#include "libft.h"

void	*ft_memcpy(void *dst, const void *src, size_t n)
{
	unsigned int			i;	
	unsigned char			*d;
	unsigned const char		*s;

	if (!dst && !src)
		return (dst);
	d = (unsigned char *) dst;
	s = (unsigned const char *) src;
	i = 0;
	while (i < n)
	{
		d[i] = s[i];
		i++;
	}
	return (dst);
}
